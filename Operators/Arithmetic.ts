//Arithmetic operators + - * / % ++ --

var x: number = 10;
var y: number = 20;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);

console.log(x++); //first assign and then increment 
console.log(++x); //first increment and then assign

console.log(y--); //first assign and then decrement 
console.log(--y); //first decrement and then assign
